#include <iostream>
#include <locale>
using namespace std;

int main()
{
	setlocale(LC_ALL, "Turkish");




	int x = 12, y = 15;
	
	int* ptr = &x;


/*--------------------------------------------------------------------------------------------------------------------*/
	int*& r = ptr;		//  Referans = ptr, veri türü = int*

	cout << r << endl;
	cout << &x << endl;

/*--------------------------------------------------------------------------------------------------------------------*/
	r = &y;

	cout << r << endl;
	cout << &y << endl;
/*--------------------------------------------------------------------------------------------------------------------*/
	(*r)++;

	cout << *r << endl;
	cout << y << endl;
/*--------------------------------------------------------------------------------------------------------------------*/
}
